- (void)loadView
{
    [super loadView];
    CGRect frame = [[UIScreen mainScreen] applicationFrame];
    
    CGRect titleRect = CGRectMake(0, 60, frame.size.width, 39);
    UILabel *tableTitle = [[UILabel alloc] initWithFrame:titleRect];
    tableTitle.textColor = [UIColor colorWithRed:.988 green:.808 blue:0 alpha:1];
    tableTitle.backgroundColor = [UIColor colorWithRed:0.408 green:0.404 blue:0.365 alpha:1];
    tableTitle.opaque = YES;
    tableTitle.font = [UIFont boldSystemFontOfSize:18];
    tableTitle.text = _Lable;
    tableTitle.textAlignment=NSTextAlignmentCenter;
    
    self.tableView.tableHeaderView = tableTitle;
    [self.tableView     reloadData];
}




